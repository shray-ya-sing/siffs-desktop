function cleanupPythonProcess() {
  if (!pythonProcess) return;

  console.log('ðŸ›‘ Attempting to clean up Python process...');
  
  try {
    if (process.platform === 'win32') {
      // On Windows, we need to kill the entire process tree
      const { exec } = require('child_process');
      const pid = pythonProcess.pid;
      
      // This command will kill the process and all its children
      exec(`taskkill /F /T /PID ${pid}`, (error: Error) => {
        if (error) {
          console.error('âŒ Failed to kill Python process tree:', error.message);
        } else {
          console.log('âœ… Successfully killed Python process tree');
        }
      });
    } else {
      // On Unix-like systems
      process.kill(-pythonProcess.pid, 'SIGTERM'); // Negative PID kills the process group
      console.log('âœ… Sent SIGTERM to Python process group');
    }
  } catch (error) {
    console.error('âŒ Error during Python process cleanup:', error);
  } finally {
    pythonProcess = null;
  }
}

function cleanupFileWatcher() {
  if (fileWatcherService) {
    console.log('ðŸ”„ Cleaning up file watcher service...');
    fileWatcherService.destroy();
    fileWatcherService = null;
    console.log('âœ… File watcher service cleaned up');
  }
}

