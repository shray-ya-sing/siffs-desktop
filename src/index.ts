/**
 * Starts the python server
 */

function startPythonServer() {

  const isDev = process.env.NODE_ENV === 'development';
  
  // Debug: Log the resource path
  if (!isDev) {
    console.log('ðŸ“ Resource path:', process.resourcesPath);
    console.log('ðŸ“ Python directory:', path.join(process.resourcesPath, 'python'));
    
    // List contents to verify
    try {
      const pythonDir = path.join(process.resourcesPath, 'python');
      if (fs.existsSync(pythonDir)) {
        console.log('ðŸ“ Python directory contents:', fs.readdirSync(pythonDir));
        
        const serverDir = path.join(pythonDir, 'python-server');
        if (fs.existsSync(serverDir)) {
          console.log('ðŸ“ Server directory contents:', fs.readdirSync(serverDir));
        }
      }
    } catch (e) {
      console.error('Error listing directory:', e);
    }
  }
  
  const pythonPath = isDev 
    ? path.join(__dirname, '../../src/python-server/venv/Scripts/python.exe') // Relies on venv to be init and active
    : path.join(process.resourcesPath, 'python-server','python-server.exe');

  console.log('ðŸ” Python server path:', pythonPath);
  
  if (!fs.existsSync(pythonPath)) {
    console.error('âŒ Python server executable not found at:', pythonPath);
    return;
  }

  const serverPath = isDev
    ? path.join(__dirname, '../../src/python-server/app.py')
    : path.join(process.resourcesPath, 'asgi.py');

  console.log(`ðŸš€ Starting Python server in ${isDev ? 'development' : 'production'} mode...`);

  try {
    if (isDev) {
      console.log('ðŸ”§ Development mode: Running with Python interpreter');
      pythonProcess = spawn(pythonPath, [serverPath], {
        stdio: ['pipe', 'pipe', 'pipe'],  // Explicitly set stdin, stdout, stderr
        shell: true,
        env: {
          ...process.env,
          FLASK_ENV: 'development',
          FLASK_DEBUG: '1',
          PYTHONIOENCODING: 'utf-8',
          PYTHONUTF8: '1',
          LC_ALL: 'en_US.UTF-8',
          LANG: 'en_US.UTF-8'
        }
      });
    } else {
      console.log('ðŸ­ Production mode: Running python as standalone executable');
      // On Windows, we don't need 'start' in production as we want to capture output
      const command = process.platform === 'win32' ? pythonPath : `"${pythonPath}"`;
      
      pythonProcess = spawn(command, [], {
        stdio: ['pipe', 'pipe', 'pipe'],
        shell: true,
        env: {
          ...process.env,
          FLASK_ENV: 'production',
          FLASK_DEBUG: '1',
          PYTHONIOENCODING: 'utf-8',
          PYTHONUTF8: '1',
          LC_ALL: 'en_US.UTF-8',
          LANG: 'en_US.UTF-8'
        }
      });
    }

    
    // Handle stdout with UTF-8 decoding
    pythonProcess.stdout.setEncoding('utf8');
    pythonProcess.stderr.setEncoding('utf8');

    // Log process events
    pythonProcess.on('error', (error: Error) => {
      console.error('âŒ Failed to start Python server:', error.message);
    });

    pythonProcess.on('exit', (code: number, signal: NodeJS.Signals) => {
      if (code === 0) {
        console.log('â„¹ï¸ Python server exited normally');
      } else if (code) {
        console.error(`âŒ Python server exited with code ${code}`);
      } else {
        console.error(`âŒ Python server was killed by signal ${signal}`);
      }
    });

    // Log stdout and stderr
    pythonProcess.stdout?.on('data', (data: Buffer) => {
      console.log(`ðŸ [Python] ${data.toString().trim()}`);
    });

    pythonProcess.stderr?.on('data', (data: Buffer) => {
      console.error(`ðŸ [Python Error] ${data.toString().trim()}`);
    });

    console.log('âœ… Python server process started with PID:', pythonProcess.pid);

  } catch (error) {
    console.error('âŒ Error starting Python server:', error);
  }
}


